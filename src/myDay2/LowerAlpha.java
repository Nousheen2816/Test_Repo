//Write a Java program to print alphabets on screen, i.e., a, b, c, ..., z; in lowercase


package myDay2;

public class LowerAlpha {
	
	 public static void main(String args[])
	  {
	    char c;
	 
	    for (c = 'a'; c <= 'z'; c++) // For upper case use 'A' and 'Z'
	      System.out.print(c+" ");
	   }
}
